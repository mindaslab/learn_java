public class MathInJava {
    public static void main(String[] args){
        System.out.println(1 + 2);
        System.out.println(1 - 2);
        System.out.println(4 * 2);
        System.out.println(5 / 2);
        System.out.println(Double.valueOf(5) / 2);
        System.out.println(5 % 2);
        System.out.println(Math.pow(3, 5));
    }
}
